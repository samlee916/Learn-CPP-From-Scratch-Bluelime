#include <iostream>

using namespace std;

//basic print statements and outputs
int main() {

    cout << "Hello World!" << endl;
    // 1, 2, 3 will be in a single line
    cout << "1, " << endl;
    cout << "2, " << endl;
    cout << "3" << endl;
    //three tab spaces and a new line
    cout << "\nApples\t\t\tBananas" << endl;
    cout << "\Hello\"" << endl;
    cout << "Fruits\t\t\t Apples" << endl;

    cout << 0.12345 << endl;
    //prints 1
    cout << true << endl;
    //prints 0
    cout << false << endl;

    return 0;

}
